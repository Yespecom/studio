export const getFeaturedPosts = (): BlogPost[] => {
  return Object.values(blogPosts).slice(0, 6)
}

export const getRecentPosts = (limit = 5): BlogPost[] => {
  return Object.values(blogPosts)
    .sort((a, b) => new Date(b.date).getTime() - new Date(a.date).getTime())
    .slice(0, limit)
}

export const searchBlogPosts = (query: string): BlogPost[] => {
  const searchTerm = query.toLowerCase()
  return Object.values(blogPosts).filter(
    (post) =>
      post.title.toLowerCase().includes(searchTerm) ||
      post.content.toLowerCase().includes(searchTerm) ||
      post.keywords.toLowerCase().includes(searchTerm) ||
      post.excerpt.toLowerCase().includes(searchTerm),
  )
}

export const getCategoryPostCount = (category: string): number => {
  if (category === "All") {
    return Object.values(blogPosts).length
  }
  return Object.values(blogPosts).filter((post) => post.category === category).length
}
export function getRelatedPosts(slug: string, category: string) {
  const allPosts = Object.values(blogPosts)
  return allPosts.filter((p) => p.category === category && p.slug !== slug).slice(0, 3)
}
